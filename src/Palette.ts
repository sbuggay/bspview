export class Palette {
    constructor(private palette: number[]) {}

    colors(): Color[] {
        const colors = [];
        for (let i = 0; i < 256; i++) {
            colors.push(
                new Color(
                    this.palette[i * 3],
                    this.palette[i * 3 + 1],
                    this.palette[i * 3 + 2]
                )
            );
        }
        return colors;
    }
}

